public class MathUtils {
  public int sum(int a, int b) {
    return a + b;
  }

  public int minus(int a, int b) {
    return a - b;
  }

  public int multipe(int a, int b) {
    return a * b;
  }

  public int devide(int a, int b) {
    return a / b;
  }
}
